package j_Value_and_Reference_Types;

public class Person {
	
	private String name;
	private int age;
	

	// Constructors
	Person(){}
	
	Person(String name) {
		this.name = name;
	}

	

	// Getters - Setters
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

	
	
	
	

}
